/// Example Feature Module - Template for New Features
///
/// This is a template showing the structure and best practices
/// for creating new isolated feature modules.
///
/// To use this template:
/// 1. Copy this folder and rename it to your feature name
/// 2. Replace "Example" with your feature name throughout
/// 3. Implement your business logic following the patterns shown
/// 4. Register provider in main.dart
/// 5. Enable feature flag in config/feature_flags.dart
///
/// File Structure:
/// - models/: Data models for the feature
/// - services/: API calls and external services
/// - providers/: State management
/// - screens/: UI screens
/// - widgets/: Reusable UI components
///

/// This file serves as documentation for the feature structure.
/// Delete this file when creating an actual feature.
class ExampleFeatureTemplate {
  static const String description = '''
  This folder demonstrates the recommended structure for isolated feature modules.

  Benefits:
  - Clear separation of concerns
  - Easy to test in isolation
  - Can be disabled via feature flags
  - No impact on existing features
  - Easy to review and maintain

  Follow the patterns shown in:
  - SAFE_FEATURE_IMPLEMENTATION_GUIDE.md (root folder)
  - Example code in the guide
  ''';
}
