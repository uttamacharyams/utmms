  _submitPartnerPreferences() async {
    try {
      final prefs = await SharedPreferences.getInstance();
      final userDataString = prefs.getString('user_data');

      if (userDataString == null) {
        _showError("User data not found. Please login again.");
        return;
      }

      final userData = jsonDecode(userDataString);
      final userId = int.tryParse(userData["id"]?.toString() ?? '0');

      if (userId == null || userId == 0) {
        _showError("Invalid user ID");
        return;
      }

      // Prepare the data as JSON
      final Map<String, dynamic> body = {
        "userid": userId.toString(),
        "minage": _minAge.toInt().toString(),
        "maxage": _maxAge.toInt().toString(),
        "minheight": _minHeight.toInt().toString(),
        "maxheight": _maxHeight.toInt().toString(),
        "maritalstatus": _selectedMaritalStatus.join(","),
        "profilewithchild": _selectedProfileWithChildren ?? "No child",
        "familytype": _selectedFamilyType.join(","),
        "religion": _selectedReligion.join(","),
        "caste": _selectedCast.join(","),
        "subcaste": _selectedsubCast.join(","),
        "mothertongue": _selectedMotherTongue.join(","),
        "horoscopebelief": _selectedHoroscopeBelief ?? "",
        "manglik": _selectedManglik ?? "",
        "country": _getCountryIds(_selectedCountry).join(","),
        "state": _getStateIds(_selectedState).join(","),
        "city": _getCityIds(_selectedCity).join(","),
        "qualification": _selectedQualification.join(","),
        "educationmedium": _selectedEducationMedium.join(","),
        "profession": _selectedProfession.join(","),
        "workingwith": _selectedWorkingWith.join(","),
        "annualincome": _selectedAnnualIncome.join(","),
        "diet": _selectedDiet.join(","),
        "smokeaccept": _selectedSmokeAcceptance ?? "",
        "drinkaccept": _selectedAlcoholAcceptance ?? "",
        "disabilityaccept": _selectedDisabilityAcceptance ?? "",
        "complexion": _selectedComplexion.join(","),
        "bodytype": _selectedBodyType.join(","),
        "otherexpectation": _otherExpectationsController.text.trim(),
      };

      print("Sending JSON data: $body");

      final response = await http.post(
        Uri.parse("${kApiBaseUrl}/Api2/user_partner.php"),
        body: jsonEncode(body),
        headers: {
          "Content-Type": "application/json; charset=UTF-8",
        },
      ).timeout(const Duration(seconds: 30));

      print("Response status: ${response.statusCode}");
      print("Response body: ${response.body}");

      if (response.statusCode == 200) {
        final result = jsonDecode(response.body);

        if (result["status"] == "success") {
          // Update page number
          _showSuccess("Partner preferences saved successfully!");
          // Navigate after a short delay
          Navigator.pop(context);
        } else {
          _showError(result["message"] ?? "Failed to update progress");
        }
      } else {
        String errorMsg = "Unknown error occurred (${response.statusCode})";
        try {
          final result = jsonDecode(response.body);
          errorMsg = result["message"] ?? errorMsg;
        } catch (_) {}
        print("Non-200 response (${response.statusCode}): ${response.body.substring(0, response.body.length.clamp(0, 500))}");
        _showError(errorMsg);
      }
    } on FormatException catch (e) {
      print("JSON Format Error: $e");
      _showError("Server response format error. Please try again.");
    } on http.ClientException catch (e) {
      print("Network Error: $e");
      _showError("Network error. Please check your connection.");
    } on TimeoutException catch (e) {
      print("Timeout Error: $e");
      _showError("Request timeout. Please try again.");
    } catch (e) {
      print("Unexpected Error: $e");
      _showError("Something went wrong. Please try again.");
    }
  }

