  Widget build(BuildContext context) {
    return SafeArea(
      child: Padding(
        padding: const EdgeInsets.all(16),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Row(
              children: [
                Expanded(
                  child: Text(
                    widget.title,
                    style: const TextStyle(
                      fontSize: 20,
                      fontWeight: FontWeight.bold,
                      color: Color(0xFFE64B37),
                    ),
                  ),
                ),
                IconButton(
                  onPressed: () => Navigator.pop(context),
                  icon: const Icon(Icons.close),
                ),
              ],
            ),
            const SizedBox(height: 12),

            Container(
              padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 8),
              decoration: BoxDecoration(
                color: const Color(0xFFE64B37).withOpacity(0.08),
                borderRadius: BorderRadius.circular(10),
                border: Border.all(
                  color: const Color(0xFFE64B37).withOpacity(0.2),
                ),
              ),
              child: Text(
                '${_currentSelection.length} selected',
                style: const TextStyle(
                  color: Color(0xFFE64B37),
                  fontWeight: FontWeight.w600,
                ),
              ),
            ),

            if (_currentSelection.isNotEmpty) ...[
              const SizedBox(height: 12),
              Wrap(
                spacing: 8,
                runSpacing: 8,
                children: _currentSelection.map((item) {
                  return Chip(
                    label: Text(item),
                    backgroundColor: const Color(0xFFE64B37).withOpacity(0.1),
                    deleteIcon: const Icon(Icons.close, size: 16),
                    onDeleted: () {
                      setState(() {
                        _currentSelection.remove(item);
                      });
                      _updateParentSelection();
                    },
                    labelStyle: const TextStyle(fontSize: 13),
                  );
                }).toList(),
              ),
            ],

            if (_showSearch) ...[
              const SizedBox(height: 12),
              TextField(
                controller: _searchController,
                autofocus: true,
                textInputAction: TextInputAction.search,
                onChanged: (_) => setState(() {}),
                decoration: InputDecoration(
                  hintText: "Search...",
                  prefixIcon: const Icon(Icons.search),
                  border: OutlineInputBorder(
                    borderRadius: BorderRadius.circular(30),
                  ),
                  contentPadding: const EdgeInsets.symmetric(horizontal: 20, vertical: 10),
                ),
              ),
            ],
            const SizedBox(height: 16),

            Expanded(
              child: _visibleOptions.isEmpty
                  ? Center(
                child: Text(
                  _showSearch && _searchController.text.trim().isNotEmpty
                      ? 'No options found'
                      : 'All options already selected',
                ),
              )
                  : ListView.builder(
                itemCount: _visibleOptions.length,
                itemBuilder: (context, index) {
                  final option = _visibleOptions[index];

                  return ListTile(
                    title: Text(option),
                    trailing: const Icon(Icons.add_circle_outline),
                    onTap: () {
                      setState(() {
                        if (option == 'Any') {
                          _currentSelection = ['Any'];
                        } else {
                          _currentSelection.remove('Any');
                          _currentSelection.add(option);
                        }
                      });
                      _updateParentSelection();
                      Navigator.pop(context);
                    },
                  );
                },
              ),
            ),

            const SizedBox(height: 16),
            Row(
              children: [
                Expanded(
                  child: OutlinedButton(
                    onPressed: () {
                      setState(() {
                        _currentSelection.clear();
                      });
                      _updateParentSelection();
                    },
                    child: const Text('Clear All'),
                  ),
                ),
                const SizedBox(width: 12),
                Expanded(
                  child: ElevatedButton(
                    onPressed: () => Navigator.pop(context),
                    style: ElevatedButton.styleFrom(
                      backgroundColor: const Color(0xFFE64B37),
                      padding: const EdgeInsets.symmetric(vertical: 16),
                      shape: RoundedRectangleBorder(
                        borderRadius: BorderRadius.circular(12),
                      ),
                    ),
                    child: const Text(
                      'Done',
                      style: TextStyle(
                        fontSize: 16,
                        fontWeight: FontWeight.w600,
                        color: Colors.white,
                      ),
                    ),
                  ),
                ),
              ],
            ),
          ],
        ),
      ),
    );
  }
}
