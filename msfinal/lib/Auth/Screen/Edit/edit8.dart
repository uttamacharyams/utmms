    final query = _searchController.text.trim().toLowerCase();

    return widget.options.where((option) {
      if (_currentSelection.contains(option)) {
        return false;
      }

      if (!_showSearch || query.isEmpty) {
        return true;
      }

      return option.toLowerCase().contains(query);
    }).toList();
  }

