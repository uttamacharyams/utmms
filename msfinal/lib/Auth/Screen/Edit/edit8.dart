  void _handleCountryChange(List<String> selectedItems) {
    setState(() {
      _selectedCountry = selectedItems;
      _errors['country'] = '';

      // Reset states and cities when countries change
      _selectedState = ['Any'];
      _selectedCity = ['Any'];
      _allStates.clear();
      _allCities.clear();
    });

    // Load states for selected countries
    _loadStatesForCountries(_getCountryIds(selectedItems));
    _updateAutoGeneratedExpectations();
  }

  void _handleStateChange(List<String> selectedItems) {
    setState(() {
      _selectedState = selectedItems;
      _errors['state'] = '';

      // Reset cities when states change
      _selectedCity = ['Any'];
      _allCities.clear();
    });

    // Load cities for selected states
    _loadCitiesForStates(_getStateIds(selectedItems));
    _updateAutoGeneratedExpectations();
  }

  void _handleCityChange(List<String> selectedItems) {
    setState(() {
      _selectedCity = selectedItems;
      _errors['city'] = '';
    });

    _updateAutoGeneratedExpectations();
  }

  void _handleMultiSelectChange(List<String> selectedItems, String fieldName, Function(List<String>) updateFunction) {
    // If "Any" is selected, clear all other selections and only keep "Any"
    if (selectedItems.contains('Any')) {
      selectedItems = ['Any'];
    }
    // If something else is selected and "Any" was previously selected, remove "Any"
    else if (selectedItems.isNotEmpty) {
      selectedItems.remove('Any');
    }

    updateFunction(selectedItems);
    _updateAutoGeneratedExpectations();
  }

  void _handleSingleSelectChange(String? value, String fieldName, Function(String?) updateFunction) {
    updateFunction(value);
    _updateAutoGeneratedExpectations();
  }

