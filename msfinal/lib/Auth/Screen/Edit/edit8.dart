  double _clampAgeValue(double value) {
    if (value < _minPreferredAgeLimit) {
      return _minPreferredAgeLimit;
    }

    if (value > _maxPreferredAgeLimit) {
      return _maxPreferredAgeLimit;
    }

    return value;
  }

  void _applyNormalizedAgeRange({
    double? minAge,
    double? maxAge,
  }) {
    final normalizedMin = _clampAgeValue(minAge ?? _minAge);
    final normalizedMax = _clampAgeValue(maxAge ?? _maxAge);

    if (normalizedMin > normalizedMax) {
      _minAge = normalizedMax;
      _maxAge = normalizedMin;
      return;
    }

    _minAge = normalizedMin;
    _maxAge = normalizedMax;
  }

  void _applyAgeBoundsFromUserData(Map<String, dynamic> userData) {
    final bounds = resolvePartnerAgePreferenceBounds(
      userData: userData,
      fallbackMaxAge: _maxPreferredAgeLimit.toInt(),
    );

    _minPreferredAgeLimit = bounds.minAge.toDouble();
    _maxPreferredAgeLimit = bounds.maxAge.toDouble();
    _applyNormalizedAgeRange();
  }

  List<String> _asPreferenceList(dynamic value) {
    if (value == null) return ['Any'];
    if (value is List) {
      final normalized = value
          .map((item) => item.toString().trim())
          .where((item) => item.isNotEmpty)
          .toList();
      return normalized.isEmpty ? ['Any'] : normalized;
    }

    final normalized = value
        .toString()
        .split(',')
        .map((item) => item.trim())
        .where((item) => item.isNotEmpty)
        .toList();
    return normalized.isEmpty ? ['Any'] : normalized;
  }

